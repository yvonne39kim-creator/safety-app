    cursor = conn.cursor()
    cnt = 0
    
    for th in massive_theories:
        cursor.execute('SELECT id FROM subjects WHERE subject_name=? AND chapter_name=?', (th['subject'], th['chapter_name']))
        row = cursor.fetchone()
        if row:
            cursor.execute('UPDATE subjects SET description=? WHERE id=?', (th['description'], row[0]))
            cnt += 1
        else:
            cursor.execute('INSERT INTO subjects (subject_name, chapter_name, description) VALUES (?, ?, ?)', (th['subject'], th['chapter_name'], th['description']))
            cnt += 1
            
    conn.commit()
    conn.close()
